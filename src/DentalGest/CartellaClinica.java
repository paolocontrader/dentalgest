    public  void PopulateData() {

// Clear table
        tb1.setModel(new DefaultTableModel());
         
// Model for Table
        DefaultTableModel model = new DefaultTableModel() {

            @Override
            public Class<?> getColumnClass(int column) {

                switch (column) {

                    case 0:

                        return Boolean.class;

                    case 1:

                        return String.class;

                    case 2:

                        return String.class;

                   default:

                        return String.class;

                }

            }

        };

        tb1.setModel(model);
        
// Add Column
        model.addColumn("");

        model.addColumn("Nome");
        
        model.addColumn("Path");

        
    String sql = "SELECT * FROM  cartella where paziente =? ORDER BY nome DESC";
        try {

            pstPop = connPop.prepareStatement(sql);
            pstPop.setString(1, paziente.getText());
            System.out.println("Paziente recuperato: "+paziente.getText());

            ResultSet recPop = pstPop.executeQuery();

            int row = 0;
            
                while ((recPop != null) && (recPop.next())) {
                   
                model.addRow(new Object[0]);

                model.setValueAt(false, row, 0); // Checkbox

                model.setValueAt(recPop.getString("nome"), row, 1);
                model.setValueAt(recPop.getString("path"), row, 2);
                
                row++;

            }
                   System.out.println("Numero righe tabella cartella_clinica: "+row);
                
            
          // tb1.removeColumn(tb1.getColumnModel().getColumn(0)); 
           
              tb1.getColumnModel().getColumn(0).setPreferredWidth(50);
            tb1.getColumnModel().getColumn(1).setPreferredWidth(200);
            tb1.getColumnModel().getColumn(2).setPreferredWidth(600);
            
            

        } catch (SQLException e) {

// TODO Auto-generated catch block
            JOptionPane.showMessageDialog(null, e.getMessage());


// TODO Auto-generated catch block

        }

    }

